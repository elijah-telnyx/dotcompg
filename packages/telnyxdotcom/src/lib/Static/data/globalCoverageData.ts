const keyMap = (key: keyof RawData) => {
  switch (key) {
    case 'Outbound_Local':
      return 'Local Calling';
    case 'Inbound_Calling':
      return 'Inbound Calling';
    case 'Emergency_Calling':
      return 'Emergency Calling';
    case '2_way_SMS':
      return '2 Way Sms';
    case 'Fax':
      return 'Fax support';
    case 'Porting':
      return 'Number Portability';
    case 'Full_PSTN_Replacement':
      return 'Full PSTN Replacement';
    default:
      /**
       * @description not over the table
       * Outbound_International**
       */
      return undefined;
  }
};

const formatRawDataObj = (obj: RawData) => {
  return Object.entries(obj).reduce((final, [key, value]) => {
    const newKey = keyMap(key as keyof typeof obj);
    if (!newKey) {
      return final;
    }
    return {
      ...final,
      [newKey]: value,
    };
  }, {});
};

const formatToApiResponse = (data: typeof rawData) =>
  Object.values(
    data.reduce((formattedData, item) => {
      const { Country, Number_Type } = item;
      const country = getCountryByName(Country);
      if (!country) {
        console.error(`Country ${Country} not found`);
        return formattedData;
      }

      const key = country.alpha2;

      if (!formattedData[key]) {
        formattedData[key] = {};
      }
      formattedData[key][Number_Type] = formatRawDataObj(item);
      formattedData[key].country = country;

      return formattedData;
    }, {} as Record<string, Record<string, Country | Record<string, string | boolean>>>)
  ).map(({ country, ...types }) => {
    return { country, types };
  });

export default formatToApiResponse(rawData);
